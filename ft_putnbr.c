#include "ft_printf.h"

void	ft_putnbr(int n, int *len)
{
	if (n == -2147483648)
		ft_putstr("-2147483648", len);
	else
	{
		if (n < 0)
		{
			n = n * -1;
			ft_putchar('-', len);
		}
		if (n > 9)
			ft_putnbr(n / 10, len);
		ft_putchar(n % 10 + '0', len);
	}
}
